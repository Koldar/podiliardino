package com.massimobono.podiliardino.model;

public class PodiliardinoException extends Exception {

	private static final long serialVersionUID = 1L;

	public PodiliardinoException() {
		super();
	}

	public PodiliardinoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public PodiliardinoException(String arg0) {
		super(arg0);
	}

	public PodiliardinoException(Throwable arg0) {
		super(arg0);
	}
	
	

}
