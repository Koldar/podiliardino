package com.massimobono.podiliardino.extensibles;

public class FormatException extends Exception {

	private static final long serialVersionUID = -2681421419453164538L;

	public FormatException() {
		super();
	}

	public FormatException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public FormatException(String arg0) {
		super(arg0);
	}

	public FormatException(Throwable arg0) {
		super(arg0);
	}
	
	

}
